public class Shape {
    protected int x;
    protected int y;

    // This is the SHAPE CONSTRUCTOR - (Default)
    public Shape() {
        this.x = 0;
        this.y = 0;
    }

    // This CONSTRUCTOR contains X & Y PARAMS
    public Shape(int x, int y) {
        this.x = x;
        this.y = y;
    }
}